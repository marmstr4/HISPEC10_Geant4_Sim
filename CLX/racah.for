	FUNCTION RACAH(AJ1,AJ2,AL2,AL1,AJ3,AL3)
	REAL*8 FCT(99),FACT(99),TERM,WOLLE2
	COMMON /FCTRLS/ FCT , FACT
C
C	COMPUTE THE RACAH COEFFICIENTS (6-J SYMBOL) W(J1,J2,L2,L1,J3,L3)
C
	RACAH=0.
	NA=INT(2.*AJ1)
	NB=INT(2.*AJ2)
	NCC=INT(2.*AL2)
	ND=INT(2.*AL1)
	NEE=INT(2.*AJ3)
	NF=INT(2.*AL3)
	NC=NEE
	NE=NCC
	N1=NA+NB+NC
	N2=ND+NE+NC
	N3=NA+NE+NF
	N4=ND+NB+NF
	IF ( MOD(N1,2) .NE. 0 ) RETURN
	IF ( MOD(N2,2) .NE. 0 ) RETURN
	IF ( MOD(N4,2) .NE. 0 ) RETURN
	N1=N1/2
	N2=N2/2
	N3=N3/2
	N4=N4/2
	N1A=N1-NC
	N2A=N2-NC
	N3A=N3-NF
	N4A=N4-NF
	L6=NE-N2A-N3A
	L7=NA-N1A-N3A
	L1=N3A+N4A+NF+1
	KMIN=MAX(0,-L6,-L7)
	KMAX=MIN(L1,N1A,N2A,N3A,N4A)
	IF ( KMAX .LT. KMIN ) RETURN
	N1B=N1-NB
	N1C=N1-NA
	N2B=N2-NE
	N2C=N2-ND
	N3B=N3-NE
	N3C=N3-NA
	N4B=N4-NB
	N4C=N4-ND
	SGN=1.
	  DO K=KMIN,KMAX
	  LL1=L1-K
	  LL2=N1A-K
	  LL3=N2A-K
	  LL4=N3A-K
	  LL5=N4A-K
	  LL6=L6+K
	  LL7=L7+K
	  TERM=FACT(LL1+1)-(FACT(K+1)+FACT(LL2+1)+FACT(LL3+1))
	  TERM=TERM-(FACT(LL4+1)+FACT(LL5+1)+FACT(LL6+1)+FACT(LL7+1))
	  WOLLE2=FACT(N1A+1)+FACT(N1B+1)+FACT(N1C+1)-FACT(N1+2)
	  WOLLE2=WOLLE2+FACT(N2A+1)+FACT(N2B+1)+FACT(N2C+1)-FACT(N2+2)
	  WOLLE2=WOLLE2+FACT(N3A+1)+FACT(N3B+1)+FACT(N3C+1)-FACT(N3+2)
	  WOLLE2=WOLLE2+FACT(N4A+1)+FACT(N4B+1)+FACT(N4C+1)-FACT(N4+2)
	  RACAH=RACAH+SGN*EXP(WOLLE2+2.*TERM)
	  SGN=-SGN
	  END DO
	IF ( MOD(KMIN,2) .NE. 0 ) RACAH=-RACAH
	RETURN
	END
